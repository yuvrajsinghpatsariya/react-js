function sum(a,b){
    let sum = a+b;
    return sum;
}
function sub(a,b){
    let sub = a-b;
    return sub;
}
function mult(a,b){
    let mult = a*b;
    return mult;
}
function div(a,b){
    let div = a/b;
    return div;
}
function mod(a,b){
    let mod = a%b;
    return mod;
}
export  {sum, sub, mult, div, mod};